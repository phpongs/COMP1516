def greet_user():
    """
    Prints a greeting to the user.
    :return: None
    """
    print("hi")
    print("have a nice day")
    print("bye")


def say_hi_to_user(name):
    """
    Prints hello and the user's name.
    :param name: the user's name
    :return: None
    """
    print("hello", name.upper())
