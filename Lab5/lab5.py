def how_many_countries():
    """
    calculate the total number of countries in the 'countries' list
    :return: int number of countries in the 'countries' list
    """
    return len(countries)


def get_name_of_longest_country():
    """
    finds and returns the name of the country with the longest name from the 'countries' list
    :return: country with the longest name
    """
    # variable to keep track of the current index, count current letter and the store longest name
    i = 0
    country_name_letter = 0
    longest_country_name = None

    while i < len(countries):                               # running loop to check all lists in countries
        if len(countries[i]) > country_name_letter:         # if country name length > current letter
            longest_country_name = countries[i]             # will assign to longest_country_name
            country_name_letter = len(countries[i])         # and it's length assigned to country_name_letter
        i += 1                                              # move to the next index
    return longest_country_name


def get_number_of_capitals_containing(substring):
    """
    finds and returns the number of capitals in the 'capitals' list that contain a given substring
    :param substring: to search for within the names of the capitals
    :return: count of capitals whose names contain the specified substring
    """
