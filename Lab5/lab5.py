    i = 0
    count = 0

    while i < len(capitals):                                # running loop to check all lists in capitals
        if substring.lower() in capitals[i].lower():        # if the argument given is inside capital name
            count += 1                                      # counting + 1
        i += 1                                              # move to the next index
    return count


